package lan

import (
	"strconv"
	"testing"
)

func TestSprint(t *testing.T) {
	sprint := Sprint("A")
	t.Log(sprint)
}

func Sprint(x any) string {
	type stringer interface {
		String() string
	}

	switch x := x.(type) {
	case stringer:
		return x.String()
	case string:
		return x
	case int:
		return strconv.Itoa(x)
	// ...similar cases for int16, uint32, and so on...
	case bool:
		if x {
			return "true"
		}
		return "false"
	default:
		// array, chan, func, map, pointer, slice, struct
		return "???"
	}
}
